weather = 'rain'

case weather
when 'rain'
puts "Use an umbrella!"
when 'snow'
	puts "wear snow pants, snow boots, winter jacket, gloves, hat."
when 'sunny'
	puts "wear sunscreen, shorts, and short sleeve shirt"
when 'cloudy' "wear sunscreen, have an umbrella along"
when 'hot'
puts "wear shorts, short sleeve shirt, drink a lot of water!"
when 'cold'
	puts "wear paints, and sweater"
when 'warm' 
	puts "wear shorts, short sleeve shirt"
when 'really cold'
	puts "wear 2 pairs of pants, a shirt, 2 sweaters, hat, gloves"
else
	puts "Hmm, well I don't know what that weather is!"
